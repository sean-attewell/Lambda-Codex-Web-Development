// Domain Names and URLs
// In 1984, a couple of decades after the first computers and networks were invented, the Domain Name Service (DNS) was introduced. 
// This was a concept where computers could distinguish themselves from one another. 
// As a result, six domains that you will probably recognize were introduced: edu (education), gov (government), mil (military), com (commercial), net (network resources), and org (organization).

// Just a few short years later, in 1991, the worldwide web network was opened to the world and incorporated the new HTML computer language. 
// HTML uses specifications for Uniform Resource Locators (URLs). 
// These URLs became (and remain) the universal standard for locating website addresses.

// By definition, a URL is a formatted text string referring to the location of a resource on a computer network (most commonly the web). 
// Typically, these resources are web pages, but they can also be text documents, graphics, programs, or pretty much anything that can be stored digitally.

// Combining this all together, we learn that computers can connect to the internet via the world wide web, locate a specific server computer through a URL and domain name, and retrieve information or resources from that server.


